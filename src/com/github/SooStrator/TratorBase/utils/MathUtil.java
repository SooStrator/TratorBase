package com.github.SooStrator.TratorBase.utils;

public class MathUtil {
	
	public static double getSquareRoot(double input) {
		return Math.sqrt(input);
	}
	
	public static double getSinus(double input) {
		return Math.sin(input);
	}
	
	public static double getCosinus(double input) {
		return Math.cos(input);
	}
	
	public static double getTangens(double input) {
		return Math.tan(input);
	}
	
	public static double getPower(double base, double power) {
		return Math.pow(base, power);
	}
	
}